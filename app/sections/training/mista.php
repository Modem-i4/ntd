<ul>
  <li>У XIV–XV ст. триває урбанізація; у містах — до 15% населення.</li>
  <li>Найбільші: Львів (&gt;10 тис.), Кам’янець і Луцьк (≈4 тис.), Київ (до 3 тис.).</li>
  <li>Типи міст: королівські (великокнязівські), приватновласницькі, церковні.</li>
</ul>

<h2>Магдебурзьке право</h2>
<ul>
  <li>Німецьке міське право самоврядування, звільняло від влади феодалів.</li>
  <li>Першим отримав Сянок (1339), далі — Львів (1356), Кам’янець (1374), Київ (1497–1499).</li>
</ul>

<h2>Органи самоврядування (магістрат)</h2>
<ul>
  <li><strong>Рада:</strong> адміністрація та цивільний суд; очолював бургомістр.</li>
  <li><strong>Лава:</strong> суд у кримінальних справах; очолював війт (часто спадковий/призначуваний).</li>
  <li>Символ самоврядування — ратуша.</li>
</ul>

<h2>Міське населення</h2>
<ul>
  <li><strong>Патриціат:</strong> заможні купці, верхівка управління.</li>
  <li><strong>Поспільство:</strong> ремісники й дрібні торгівці.</li>
  <li><strong>Плебс:</strong> біднота, наймити.</li>
</ul>

<ul>
  <li>Магдебурзьке право часто було обмеженим: православних українців усували від управління, війтів іноді призначали.</li>
</ul>
