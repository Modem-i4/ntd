<h1>Терміни</h1>

<ul>
  <li><strong>Натуральне господарство</strong> — виробництво для власного споживання.</li>
  <li><strong>Оброк</strong> — плата продуктами/грошима феодалу.</li>
  <li><strong>Панщина</strong> — безоплатне відпрацювання робіт на землях пана/церкви/держави.</li>
  <li><strong>Латифундія</strong> — велике приватне землеволодіння з працею залежних селян.</li>
  <li><strong>Фільварок</strong> — багатогалузеве панське господарство, зорієнтоване на ринок.</li>
  <li><strong>Кріпацтво (закріпачення)</strong> — прикріплення селян до землі й влади землевласника.</li>
  <li><strong>Магдебурзьке право</strong> — міське самоврядування за німецьким зразком.</li>
  <li><strong>Цех / гільдія</strong> — корпорації ремісників / купців.</li>
  <li><strong>Ярмарок</strong> — періодичний великий торг.</li>
</ul>
