<ul>
  <li>Погіршення становища селян викликало спротив; основна форма — втечі на південні та східні землі (Дике Поле).</li>
  <li><strong>Повстання 1431–1434 рр.:</strong> масові виступи на Західному Поділлі проти польських порядків.</li>
  <li><strong>Повстання Мухи (1490–1492):</strong> охопило Буковину, Галичину, Західне Поділля; захоплено Снятин, Коломию, Галич; поразка під Рогатином; ватажок Андрій Боруля (ймовірно Муха) страчений.</li>
  <li><strong>Опришки:</strong> з 1529 р. — народні месники у Карпатах, нападали на маєтки шляхти.</li>
</ul>
