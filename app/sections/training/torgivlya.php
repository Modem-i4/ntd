<h1>Торгівля</h1>

<ul>
  <li>Розвивалася внутрішня й зовнішня торгівля; купці об’єднувалися в гільдії.</li>
  <li><strong>Форми торгівлі:</strong> ярмарки (Львів, Київ, Галич тощо), торги (кілька разів на тиждень), щоденна торгівля у крамницях.</li>
  <li>Головні шляхи пролягали через Галичину, Поділля, Волинь, з’єднуючи Європу зі Сходом.</li>
  <li><strong>Експорт:</strong> зерно, худоба, віск, мед, сіль, деревина, хутро.</li>
  <li><strong>Імпорт:</strong> тканини (сукно, оксамит), одяг, залізо, зброя, папір.</li>
  <li>Падіння Константинополя (1453) переорієнтувало європейську торгівлю зерном на Балтику, стимулювавши його виробництво на українських землях.</li>
</ul>
