<ul>
  <li>Шляхетський стан був неоднорідним і поділявся за заможністю та впливом.</li>
  <li><strong>Магнати:</strong> найзаможніші землевласники, верхівка шляхти.</li>
  <li><strong>Князі:</strong> почесний титул.
    <ul>
      <li><em>Княжата головні:</em> найвпливовіші роди (Острозькі, Вишневецькі, Збаразькі тощо); власні суди, податки, приватні армії (до 10–15 тис.).</li>
      <li><em>Княжата-повітовники:</em> середні та дрібні князі, без значних привілеїв, підпорядковані місцевій владі.</li>
    </ul>
  </li>
  <li><strong>Пани:</strong> титул середньої та вищої шляхти; земельна власність, піддані, економічна самостійність.</li>
  <li><strong>Дрібна шляхта:</strong> найчисленніша категорія.
    <ul>
      <li><em>Зем’яни:</em> власники невеликих ділянок, часто обробляли їх самі.</li>
      <li><em>Чиншова шляхта:</em> безземельні, орендували землю у магнатів за чинш.</li>
      <li><em>Шляхта-голота:</em> найбідніші; служба у військах чи при дворах магнатів.</li>
    </ul>
  </li>
</ul>

<h1>Шляхта: Загальні права та привілеї</h1>

<ul>
  <li><strong>Особиста недоторканність:</strong> ув’язнення лише за рішенням шляхетського суду.</li>
  <li><strong>Посилений судовий захист:</strong> злочини проти шляхтича каралися суворіше.</li>
  <li><strong>Владні повноваження:</strong> широкі управлінські та судові права над підданими.</li>
  <li><strong>Земельна власність:</strong> спадковість і вільне розпорядження землею.</li>
  <li><strong>Податкові пільги:</strong> звільнення від більшості податків.</li>
  <li><strong>Політичні права:</strong> виключне право на державні посади та участь у політичному житті.</li>
</ul>
