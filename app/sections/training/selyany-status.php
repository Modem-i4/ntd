<h2>Суспільний устрій</h2>
<ul>
  <li>Селяни жили громадами (селами) під проводом отаманів або тивунів.</li>
  <li>Кілька сіл об’єднувалися у волость (копу) на чолі зі старостою.</li>
  <li>Збори копи та копний суд вирішували важливі справи.</li>
</ul>

<h2>Правовий статус</h2>
<ul>
  <li><strong>«Похожі» (вільні):</strong> мали право піти від свого феодала.</li>
  <li><strong>«Непохожі» («отчичі»):</strong> прикріплені до землі, без права відходу без дозволу.</li>
</ul>

<h2>Соціальні групи селян</h2>
<ul>
  <li><strong>Слуги:</strong> переважно несли військову службу.</li>
  <li><strong>Данники:</strong> сплачували данину грошима або продуктами.</li>
  <li><strong>Тяглі:</strong> працювали на землі феодала зі своїм інвентарем і худобою.</li>
  <li><strong>Челядь:</strong> цілком залежні дворові слуги.</li>
</ul>

<h2>Повинності та податки</h2>
<ul>
  <li><strong>Державні:</strong> грошові збори на військо, будівництво/ремонт замків, мостів, доріг.</li>
  <li><strong>Церковні:</strong> десятина (десята частина прибутків).</li>
  <li><strong>На користь феодала:</strong>
    <ul>
      <li><strong>Оброк:</strong> частина продуктів (зерно, худоба, мед тощо).</li>
      <li><strong>Панщина:</strong> безоплатна праця на землі феодала (XV ст. — ~14 днів/рік; згодом — 1–2 дні/тиждень).</li>
    </ul>
  </li>
</ul>
