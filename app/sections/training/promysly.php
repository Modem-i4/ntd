<p>Сільське господарство було основною галуззю економіки, що переважно мала натуральний характер (продукція вироблялася для власного споживання).</p>

<h2>Основні напрямки</h2>
<ul>
  <li><strong>Землеробство:</strong> поширювалися трипільна (Галичина, Волинь, Поділля) та двопільна і перелогова (Полісся) системи обробітку землі.</li>
  <li><strong>Колісний плуг:</strong> його використання дозволило освоювати нові землі. Вирощували переважно жито, а також пшеницю, ячмінь; технічні культури — льон, коноплі, хміль.</li>
  <li><strong>Скотарство:</strong> розведення великої рогатої худоби, зокрема на продаж у західноєвропейські країни; воли часто були засобом розрахунку.</li>
  <li><strong>Промисли:</strong> бортництво (згодом присадибне бджільництво), рибальство, мисливство (привілей феодалів), солеваріння (Карпати, Причорномор'я), переробка с/г продукції.</li>
</ul>

<h2>Фільварки та закріпачення селян</h2>
<ul>
  <li>Зростання попиту на зерно та м’ясо в Західній Європі спричинило появу фільварків — великих панських господарств, орієнтованих на ринок і працю залежних селян.</li>
  <li>Для створення фільварків феодали відбирали селянські землі, що супроводжувалося посиленням панщини.</li>
  <li><strong>Процес закріпачення:</strong>
    <ul>
      <li>1435–1447 рр.: обмеження права переходу (у Галичині — лише на Різдво після викупу; у ВКЛ — заборона приймати селян-утікачів).</li>
      <li>1496 р.: із села міг піти лише один селянин на рік.</li>
      <li>1503 р.: заборона переселення без дозволу феодала.</li>
      <li>Остаточно: Польське королівство — 1573 р.; ВКЛ — 1588 р. (Третій Литовський статут).</li>
    </ul>
  </li>
  <li>У ВКЛ закріпачення відбувалося повільніше через татарські набіги і нестачу робочих рук.</li>
</ul>
