<p>Духовенство — привілейований стан із власною внутрішньою організацією.</p>

<h2>Поділ за способом життя</h2>
<ul>
  <li><strong>Біле духовенство:</strong> священнослужителі без чернечого постригу (парафіяльні священники, єпископи, митрополит).</li>
  <li><strong>Чорне духовенство:</strong> монахи та настоятелі монастирів.</li>
</ul>

<h2>Поділ за ієрархією</h2>
<ul>
  <li><strong>Вищі ієрархи:</strong> митрополит, єпископи; зазвичай зі знатних родів, призначалися з дозволу правителя.</li>
  <li><strong>Нижчі ієрархи:</strong> парафіяльні священники, чиє становище значною мірою залежало від шляхти-землевласника.</li>
</ul>

<ul>
  <li>З кінця XVI ст. православне духовенство зазнавало утисків через посилення позицій католицизму.</li>
</ul>
