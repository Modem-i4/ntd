    <b>Самореалізація</b> — це процес розкриття людиною свого природного потенціалу.
    Більшість людей використовує лише близько 10% своїх можливостей, тому над їх
    розкриттям потрібно постійно працювати.
  </p>

  <h3>Піраміда потреб Маслоу</h3>
  <p>
    Український емігрант Маслов (Маслоу) зажив собі слави, склавши найвідомішу «піраміду потреб», що описує розвиток особистості.
  </p>
  <p><b>Згідно неї, особистість «прокачується» через задоволення потреб:</b></p>
  <ul>
    <li><b>Фізіологічні</b> (їжа, сон).</li>
    <li><b>Безпека</b> (впевненість у майбутньому).</li>
    <li><b>Соціальні</b> (спілкування, належність до групи).</li>
    <li><b>Престижні</b> (повага, визнання, статус).</li>
    <li><b>Самовираження</b> (творчість, реалізація цілей).</li>
  </ul>
  <p>☆ <i>«Плеяда» ризикувала безпекою – базовою потребою, щоб задовольнити високу потребу – самовираження.</i></p>

  <p>
    <b>Рух до вершини</b>: розвиток особистості залежить від особистих якостей
    (працелюбність, наполегливість) та життєвих обставин.
  </p>
