  <h3>Здібності</h3>
  <p>
    Здібності — це схильність до певної діяльності. Але вони не приносять плодів без праці.
  </p>
  <p>Види здібностей:</p>
  <ul>
    <li><strong>Загальні:</strong> розум, пам’ять, мова.</li>
    <li><strong>Спеціальні:</strong> музичні, математичні, спортивні.</li>
    <li><strong>Теоретичні та практичні:</strong> вміння планувати та втілювати.</li>
    <li><strong>Творчі:</strong> здатність створювати щось нове.</li>
  </ul>
