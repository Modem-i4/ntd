  <p>
    <b>Ідентичність</b> — це відповідь людини на запитання <b>«Хто я?»</b>. Усвідомлення своєї
    унікальності, але водночас схожості з іншими людьми. Свого місця у світі.
  </p>
  <p>
    Ідентичність формується протягом життя
    під впливом родини, друзів та власного досвіду.
  </p>

  <h3>Ми ідентифікуємо себе з двох точок зору:</h3>
  <ul>
    <li>
      <strong>Особиста ідентичність</strong> — те, що робить нас унікальними
      (власні цінності, здібності, життєва історія).
    </li>
    <li>
      <strong>Групова ідентичність</strong> — те, що поєднує нас з іншими
      (національність, стать, місце проживання, соціальні ролі — «донька» чи «активістка»).
    </li>
  </ul>

