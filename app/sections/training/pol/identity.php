  <h3>Громадянська ідентичність</h3>
  <p>
    Громадянська ідентичність — це ототожнення себе з <b>Україною</b> та
    <b>народом</b>. Вона об’єднує людей різного походження, віри чи статі навколо
    цінностей, культури та історії і передбачає в тому числі відповідальність за долю країни.
  </p>

