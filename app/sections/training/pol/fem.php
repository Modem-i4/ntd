<h2>Ідеї: фемінізм і емансипація</h2>
<p>
  Наприкінці ХІХ століття в російській імперії «нормальним» життєвим шляхом для жінки
  вважають <b>вдалий шлюб</b>. Юридично вона залежить від батька чи чоловіка, доступ до освіти
  й професій обмежений, участь у громадському житті засуджують як «нежіночу».
</p>
<p>
  На цьому тлі <b>дім Косачів</b> і постать <b>Олени Пчілки</b> виглядають як революція.
  Вона не «домогосподарка при чоловікові», а письменниця, видавчиня,
  <i>організаторка культурного життя</i>. Її доньки, зокрема і <b>Леся</b>, вчать мови, читають
  книжки і мають власну думку, а не «готуються до ролі дружини».
</p>
<p>
  <b>Леся Українка</b> росте в цій атмосфері, не погоджується на роль «тихої хворої дівчини»,
  яку бережуть від світу. Вона пише, перекладає, сперечається 
  про <i>політику, національні й жіночі права</i>.
</p>
<p>
  У її текстах з’являються <b>героїні</b>, які хочуть
  творити, любити й діяти за власним вибором, а не за тим, «як заведено».
</p>
<p>
  В ті часи <i>«фемінізм»</i> називали частіше <i>«емансипація»</i>. <br/>
  <b>Різниця у перекладі:</b> <i>«емансипація»</i> – це «визволення з-під влади чоловіка».
  Буквально такі права доводиться виборювати героїням. <br/>
  <i>«Фемінізм»</i> же – це подальший розвиток цієї ідеї, «жіночий рух».
</p>
