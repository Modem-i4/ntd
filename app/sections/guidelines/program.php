<h1>Програма підвищення кваліфікації педагогічних та науково-педагогічних працівників закладів освіти.</h1>

<b>Курс</b> «Впровадження гейміфікації у процес вивчення історії України та громадянської освіти (на базі гри «Нова Традиція»)»

<h2>Загальні положення та актуальність</h2>
<p>Історія для України — це не просто шкільний предмет, а опора ідентичності, інструмент збереження пам’яті, культурної тяглості й стійкості суспільства. В умовах війни й інформаційних викликів саме історична та громадянська освіта у поєднанні формують аспекти національної ідентичності, допомагають учням розрізняти факти й наративи, розкривають важливість громадянської участі тощо. Водночас школа проходить через стрімку діджиталізацію: смартфони та онлайн-сервіси з «відволікаючих елементів» мають ставати керованими навчальними інструментами, що працюють на конкретні результати НУШ.</p>
<p>Гра «Нова Традиція» комплексно відповідає на сучасні виклики. Її ядро — вивчення історії та громадянської освіти: засвоєння нового матеріалу відповідно до вимог НУШ, підсилення інтересу до минулого та знайомство з культурною спадщиною та концептами громадянської освіти, що формувалися у відповідні періоди. Паралельно, завдяки продуманим механікам і завданням, гра розвиває критичне мислення й стимулює громадянську активність учнів.</p>
<p>Поглянемо на ці аспекти окремо. Гра «Нова Традиція» орієнтована на вивчення історії та громадянської освіти. Учні взаємодіють із реальними історичними діячами у сюжетах, «чують» ключові факти та позиції з їхніх реплік, поглиблюють знання через вбудовані нотатки-пояснення та джерельні довідки, а засвоєння перевіряють внутрішньоігровими тестами. Така структура дає чітку траєкторію: контекст → подія/персона → доказ → коротка перевірка → рефлексія.</p>
<p>Вона підсилює критичне мислення через завдання на аналіз, зіставлення різних поглядів, виявлення маніпуляцій і аргументоване ухвалення рішень у межах ігрових сценаріїв. Одночасно стимулює громадянську активність: учні відпрацьовують навички співпраці, дискусії, локальної ініціативи та відповідальності.</p>
<p>Окремий акцент — збереження культурної спадщини. У грі використано реальні експонати відповідних періодів. Для кожного об’єкта передбачено коротку довідку, контекст появи й навігацію до джерела: учні можуть перейти на сайт музею/архіву чи дізнатися, у якому закладі експонується предмет, і поглибити вивчення теми поза уроком.</p>
<p>Для вчителя інструмент інтегрується органічно: готові сценарії та міні-завдання, просте налаштування сесій, безпечні правила роботи з телефонами, шаблони рубрик і рефлексій. Це зменшує підготовче навантаження, забезпечує прозорі критерії оцінювання й робить цифрове середовище керованим. У підсумку «Нова Традиція» перетворює урок історії чи громадянської освіти на живий, доказовий та практикоорієнтований досвід, який одночасно розвиває зацікавлення змістом, критичне мислення та активну громадянську позицію.</p>

<h2>Цільова авдиторія</h2>
<p>Педагогічні (науково-педагогічні) працівники/-ці, насамперед учителі/-ьки історії України та громадянської освіти; у тому числі заступники/-ці директорів з НВР/НМР, методисти/-ки та керівники/-ці методоб’єднань, педагоги/-ні-організатори, класні керівники/-ці; представники/-ці органів управління освітою різних рівнів і суб’єкти підвищення кваліфікації, а також інші особи, зацікавлені у впровадженні діджиталізації та гейміфікації у викладанні історії й суміжних курсів.</p>

<h2>Мета курсу</h2>
<p>Сформувати в педагогічних та науково-педагогічних працівників закладів освіти (учителів історії та громадянської освіти) цілісну, практикоорієнтовану готовність планувати, проводити та оцінювати уроки й міні-курси з використанням гри «Нова Традиція» як цифрового дидактичного інструменту — так, щоб це органічно вбудовувалося у навчальну програму НУШ, підсилювало зацікавлення історією, розвивало критичне мислення та громадянську свідомість учнів і водночас спрощувало роботу вчителя.</p>

<h2>Перелік компетентностей, що вдосконалюватимуться/набуватимуться</h2>
<p><b>Загальні компетентності:</b> громадянська й правова свідомість; критичне та системне мислення; комунікація й командна взаємодія; цифрова компетентність; ініціативність та інноваційність; культурна ідентичність і міжкультурність; самоорганізація й тайм-менеджмент; академічна доброчесність та етика.</p>
<p><b>Фахові (предметно-методичні) компетентності:</b> використання гри «Нова Традиція» у викладанні історії та громадянської освіти; робота з історичними джерелами, з компетенціями громадянської освіти й культурною спадщиною; розвиток критичного мислення учнів; педагогічний дизайн і міжпредметна інтеграція; формувальне та підсумкове оцінювання; клас-менеджмент у цифровому середовищі; безпека та захист даних; оцінювально-аналітична й рефлексивна компетентності; управлінсько-методична й лідерська компетентності.</p>

<h2>Обсяг курсу</h2>
<p>Загальний обсяг: 0,2 кредиту ЄКТС (6 годин); перерахунок: 1 кредит ЄКТС = 30 годин.</p>
<p>Модуль 1: 0,1 кредиту ЄКТС (3 години).</p>
<p>Модуль 2: 0,1 кредиту ЄКТС (3 години).</p>

<h2>Форма навчання</h2>
<p>Очна або дистанційна (онлайн) робота (що включає лекцію, демонстрацію та мікропрактику) та самостійна робота під кураторством.</p>
<p>Кураторство самостійної роботи: інструктаж і чек-лист завдань; асинхронний коментар до виконаних завдань у месенджері/ел. поштою; фінальна рекомендація щодо вдосконалення матеріалів уроку.</p>

<h2>Форма підтвердження результатів навчання</h2>
<p>Сертифікат про успішне завершення з указаним обсягом годин / кредитів ЄКТС окремо за кожним модулем або зведений сертифікат за два модулі. Автентичність сертифіката перевіряється у публічному реєстрі виданих документів; посилання для підтвердження неспростовності сертифіката зазначено в його складі.</p>

<h2>Навчально-тематичний план</h2>

<h3>Модуль 1. «Старт із грою “Нова Традиція”: методика, специфіка використання та перший урок»</h3>
<p><b>Обсяг:</b> 0,1 кредиту ЄКТС (3 год)</p>
<p><b>Форма роботи:</b> очно або дистанційно (онлайн) — лекція/демонстрація/мікропрактика (2 год); самостійна робота під кураторством (1 год).</p>
<p><b>Загальний опис:</b> Модуль дає вчителю швидке й цілісне занурення у дидактику та функціонал гри «Нова Традиція» для уроків історії України та громадянської освіти: як добирати сценарії під тему/період, працювати з історичними діячами, подіями та експонатами в належному контексті, інтегрувати внутрішньоігрові нотатки й тести для формувального оцінювання, а також забезпечувати безпечне використання гаджетів і академічну доброчесність. Під час очної або дистанційної мікропрактики учасники запускають коротку сесію, відпрацьовують інструкції й збирають «швидкий фідбек», аби побачити логіку «ціль → механіка → результат». Далі в межах самостійної роботи вчитель проводить реальний урок у своєму класі за обраним готовим сценарієм, організовує коротку рефлексію з учнями, збирає анонімізовані свідчення (скріншоти/фотографії/чек-лист спостережень) і надсилає куратору звіт із висновками та пропозиціями для вдосконалення.</p>
<p><b>Зміст очної / дистанційної (онлайн) частини (2 год):</b> гейміфікація в історичній освіті; структура та можливості гри (ролі, сценарії, внутрішньоігрові тести/чекпоінти, нотатки-пояснення); критичне мислення в ігрових завданнях; інтеграція культурної спадщини; правила безпеки та етики; міні-апробація (демо-сесія з коментарем).</p>
<p><b>Зміст самостійної роботи (1 год):</b> проведення уроку за готовим сценарієм гри «Нова Традиція»; організація короткої учнівської рефлексії (усної/письмової); збір відгуків щодо інтеграції гри в урок і загальних вражень; узагальнення висновків (чек-лист/короткий звіт); надсилання фідбеку куратору.</p>
<p><b>Очікувані результати:</b> учасник(ця) налаштовує гру та добирає готовий сценарій під тему/період; розуміє модель і ключові механіки гри та специфіку їх використання на уроці; застосовує методичні рекомендації для підвищення ефективності уроку й результатів учнів; інтегрує гру в календарно-тематичне планування та поєднує з іншими курсами/суміжними навчальними темами; проводить один реальний урок; організовує коротку рефлексію й збирає відгуки учнів; використовує внутрішньоігрові тести/чекпоінти для формувального оцінювання.</p>

<h3>Модуль 2. «Поглиблена практика: інтеграція гри до уроку, досвід застосування, оцінювання та рефлексія»</h3>
<p><b>Обсяг:</b> 0,1 кредиту ЄКТС (3 год)</p>
<p><b>Форма роботи:</b> Самостійна робота під кураторством (3 год)</p>
<p><b>Загальний опис:</b> Модуль переводить базові навички у системну практику: учитель проводить міні-цикл із 3 взаємопов’язаних активностей виключно на основі готових сценаріїв гри «Нова Традиція». Усі компоненти (завдання на критичне мислення, робота з джерелами, культурною спадщиною та артефактами, внутрішньоігрові чекпоінти/тести, нотатки-пояснення, навігація до музейних/архівних ресурсів) уже вбудовані в гру. Завдання вчителя — коректно запустити сценарії, дотриматися методичних рекомендацій, забезпечити безпечні умови, зібрати свідчення навчального прогресу та організувати учнівську рефлексію. Після виконання формується стислий пакет матеріалів для зворотного зв’язку з куратором.</p>
<p><b>Зміст самостійної роботи (3 год):</b> проведення міні-циклу з 3 активностей за обраними готовими сценаріями; використання вбудованих елементів гри (аналітичні запитання, джерела, артефакти, нотатки-пояснення, тести/чекпоінти) у повному обсязі; організація короткої учнівської рефлексії; збір анонімізованих аспектів виконання; підготовка короткої власної рефлексії (що спрацювало/що вдосконалити) та надсилання фідбек-пакету куратору.</p>
<p><b>Очікувані результати:</b> проведено міні-цикл 3 активностей повністю на ігрових сценаріях; коректно застосовано вбудовані завдання на критичне мислення, роботу з джерелами й культурною спадщиною; використано внутрішньоігрові чекпоінти/тести для формувального оцінювання; організовано учнівську рефлексію; підготовлено й надіслано куратору пакет матеріалів (плани проведення, вибірка результатів, коротка рефлексія вчителя).</p>

