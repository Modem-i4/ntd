  <h2 class="text-xl font-bold">Персонажі:</h2>

  <p><b>Пилип Орлик (роки життя: 1672–1742).</b> Український державний діяч, генеральний писар при Івані Мазепі; автор «Договорів і постановлення прав і вольностей Війська Запорозького» (Конституції 1710), гетьман в еміграції. <br><b>Роль у сюжеті:</b> ініціатор і носій «писаного закону» як вищого за посаду правителя; презентує проєкт і обґрунтовує поділ влади та підзвітність.</p>

  <p><b>Костянтин (Кость) Гордієнко (роки життя: бл. 1660–1733).</b> Кошовий отаман Запорозької Січі, уклав воєнний союз із Іваном Мазепою і Гетьманщиною у війні проти московії. Створив та очолив Кам'янську Січ, певний час очолював Олешківську Січ. <br><b>Роль у сюжеті:</b> виразник позиції запорожців і «голосу меншості»; наполягає на правах товариства, рівності перед законом і обмеженні влади гетьмана.</p>

  <p><b>Генеральна та полкова старшина (збірний образ).</b> Вищий прошарок козацької адміністрації (генеральний обозний, суддя, писар тощо; полковники). <br><b>Роль у сюжеті:</b> носії «представницької демократії» — узгоджують процедури, формулюють норми підзвітності, забезпечують баланс гетьманської влади.</p>

  <p><b>Запорозьке товариство / козацька рада (збірний образ).</b> Громада воїнів-громадян, що безпосередньо бере участь у раді й ухваленні рішень. <br><b>Роль у сюжеті:</b> джерело народовладдя; саме рада надає мандат більшості, зберігаючи право меншості на окрему позицію.</p>

  <p><b>Рядовий козак (збірний образ).</b> Представник основної частини козацького товариства, учасник ради з правом голосу. <br><b>Роль у сюжеті:</b> уособлює простих учасників громади, чиї голоси формують волю більшості.</p>
