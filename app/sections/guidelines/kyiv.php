  <p><b>Тема:</b> Соціально-економічне життя українських земель у другій половині XIV – XV ст.</p>

  <p><b>Мета:</b> Сформувати уявлення про господарський розвиток українських земель у другій половині XIV – XV ст.; показати роль міст, торгівлі, ремесел і міського самоврядування, соціальну структуру суспільства та початок процесу закріпачення селян.</p>

  <p><b>В результаті проходження гри учні:</b></p>
  <ul class="list-disc pl-6 space-y-1">
    <li>пояснюють особливості торгівлі українських земель у XIV–XV ст.;</li>
    <li>характеризують розвиток міст і зміст Магдебурзького права: типи міст, органи міського самоврядування тощо;</li>
    <li>описують соціальну структуру суспільства: міське населення, становище селян та основні повинності;</li>
    <li>аналізують роль ремесел і цехів у міському житті;</li>
    <li>пояснюють причини появи фільварків і посилення панщини, порівнюють особливості процесу закріпачення селян у Королівстві Польському та Великому князівстві Литовському.</li>
  </ul>

