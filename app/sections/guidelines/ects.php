<h2>За що видаються бали ЄКТС?</h2>
<p>Ви можете пройти очне або онлайн-навчання та провести 1 ігрове заняття, щоб отримати <b>0,1 бал ЄКТС (3 години)</b> навантаження за <b>перший модуль</b> курсу.</p>
<p>Продовживши навчання, ви можете пройти <b>другий модуль</b> курсу для здобуття ще <b>0,1 балу ЄКТС</b>. Він передбачає самостійне опрацювання та практичне проведення занять у класі.</p>

<h2>Як використати сертифікат?</h2>
<p>Сертифікат ви отримаєте <b>електронно або (на вимогу) поштою</b>, а також зможете завжди його завантажити з <a href="/guidelines/registry">реєстру</a>.</p>
<p>Отриманий сертифікат ви маєте надати педраді протягом 1 місяця з дати його видання.</p>

<h2>Якими нормативними актами це регулюється?</h2>
<p>Постанова <a href="https://zakon.rada.gov.ua/laws/show/800-2019-%D0%BF#Text" target="_blank">КМУ № 800 від 21.08.2019, п. 24</a> — <i>“Визнання результатів підвищення кваліфікації педагогічних і науково-педагогічних працівників”.</i></p>

