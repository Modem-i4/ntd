<h2>Персонажі:</h2>

<p><b>Гравець, Головний герой, Інтерчасер (у грі: посланець)</b></p>
<p>Коротко про «особу». Посланець із місією до двору Данила; має право спілкуватися з ключовими особами та передавати звістки.</p>
<p>Роль у сюжеті. Збирає думки бояр і рідні Данила, передає пропозицію Міндовга про шлюб, допомагає Данилу зважити рішення щодо союзів і спадкоємців.</p>

<p><b>Данило Романович</b></p>
<p>Коротко про особу. Князь Волині й Галича, об’єднав Волинсько-Галицьку державу (1238 р.), здобув перемогу під Ярославом (1245 р.), визнавався в Європі як король Русі (коронація 1253 р.). Провадив військові реформи, розвивав міста й торгівлю, укріплював Холм як столицю, вів активну дипломатію з Польщею, Угорщиною, Литвою та Заходом; у відносинах із Ордою поєднував опір і вимушені компроміси.</p>
<p>Роль у сюжеті. Приймає посланця після коронації, зважує шлюбний союз із Литвою, пояснює політику щодо Орди та сусідів, оголошує рішення про спадкоємців.</p>

<p><b>Василько Романович (роки життя: 1203–1269 рр.), князь володимирський</b></p>
<p>Коротко про особу. Молодший брат Данила, співправитель Волинсько-Галицької держави; утримував порядок на Волині, підтримував військові та адміністративні реформи, відомий опікою над літописанням.</p>
<p>Роль у сюжеті. Ділиться сімейною історією, підказує аргументи для переконання Данила, підтримує ідею шлюбного союзу та злагоди між гілками династії.</p>

<p><b>Лев Данилович (роки життя: бл. 1228–1301 рр.), князь галицький</b></p>
<p>Коротко про особу. Син Данила; правив Галицькою землею (з 1264 р.), одружений з угорською принцесою Констанцією, пов’язаний із заснуванням і піднесенням Львова; провадив активну зовнішню політику на польсько-литовсько-угорському напрямку.</p>
<p>Роль у сюжеті. Порушує питання спадкування, чекає на рішення батька, взаємодіє з посланцем щодо майбутнього династії.</p>

<p><b>Боярин</b></p>
<p>Коротко про «особу». Узагальнений історичний образ галицького боярина. Представник галицько-волинської знаті XIII ст.; впливові роди формували угруповання, змагалися за владу, інколи спиралися на іноземну підтримку, що загрожувало єдності держави.</p>
<p>Роль у сюжеті. Пояснює минулі чвари, битву під Ярославом (1245 р.), оцінює теперішню вірність бояр і ставлення до спадкоємців.</p>

<p><b>Вояка</b></p>
<p>Коротко про «особу». Узагальнений історичний образ галицького воїна. Боєць княжого війська Русі XIII ст.: представник дружинної кінноти чи міського ополчення; займався обороною укріплених міст та участю у походах.</p>
<p>Роль у сюжеті. Описує реформи війська (важкоозброєні, арбалетники, змішана кіннота), значення фортець і міської економіки для стійкості держави.</p>
