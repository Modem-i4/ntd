<p><b>Тема:</b> Волинсько-Галицька держава (Королівство Руське) у першій половині — середині XIII ст. Правління Данила Романовича.</p>

<p><b>Мета:</b> Сформувати уявлення про становлення та зміцнення Волинсько-Галицької держави за Данила Романовича; розкрити обставини і значення коронації 1253 р., взаємини із Заходом та особливості внутрішньої політики.</p>

<p><b>В результаті проходження гри учні:</b></p>
<ul>
  <li>Пояснюють обставини та значення коронації Данила Романовича (міжнародна легітимація, роль Заходу) і розуміють вплив королівського титулу на статус Русі.</li>
  <li>Аналізують протистояння із Золотою Ордою та пояснюють баланс між збройним опором і дипломатією; розуміють, як походи, вимоги ханів і воєнні кампанії впливали на самостійність держави.</li>
  <li>Характеризують дипломатичні зв’язки та союзи з Польщею, Угорщиною, Литвою і пояснюють роль династичних шлюбів та міжнародної дипломатії у збереженні територій і безпеки.</li>
  <li>Описують внутрішню політику Данила Романовича: співправління з братом Васильком, військові реформи, фортифікації, підтримку міст і торгівлі, пояснюють кроки з централізації влади.</li>
  <li>Оцінюють боротьбу за владу з боярською опозицією та пояснюють формування керованої держави в умовах прикордоння між Сходом і Заходом.</li>
</ul>

<p><b>Тема сюжету (оповідь):</b> Аудієнція в Данила Романовича в Галичі після коронації, місія щодо династичного шлюбу та союзу із Міндовгом. (1253 р.).</p>

