  <h2 class="text-xl font-bold">Персонажі:</h2>

  <p><b>Головний герой, Інтерчасер.</b> Ігровий образ учня/користувача. <br><b>Роль у сюжеті:</b> супроводжує героїв у ключових сценах, ставить уточнювальні запитання, “підсвічує” вибір і наслідки рішень, допомагає поєднати особисті історії персонажів із ширшим контекстом часу.</p>

  <p><b>Леся Українка (1871–1913).</b> Українська поетеса, драматургиня, перекладачка й публіцистка; одна з ключових постатей української модерної літератури. Походила з родини Косачів, отримала ґрунтовну домашню освіту, багато перекладала, активно працювала в культурному русі попри важку хворобу. <br><b>Роль у сюжеті:</b> центральна героїня, навколо якої розгортається дія: веде розмови про вибір шляху, працю, цінності, підтримку однодумців і про те, як діяти в умовах цензури та суспільного тиску.</p>

  <p><b>Жандарм (узагальнений образ представника імперської поліції кінця XIX ст.).</b> Типовий представник жандармерії Російської імперії — структури політичного нагляду, яка стежила за зібраннями, поширенням текстів і громадською активністю. <br><b>Роль у сюжеті:</b> уособлює державний контроль і тиск: створює відчуття небезпеки, демонструє межі дозволеного та те, як цензура й нагляд впливають на поведінку людей.</p>

  <p><b>Людмила Старицька-Черняхівська (1868–1941).</b> Українська письменниця, драматургиня та громадська діячка; представниця київського культурного середовища, пов’язаного з розвитком української літератури й театру. Учасниця інтелігентських осередків, де підтримували українське слово та освіту. <br><b>Роль у сюжеті:</b> постає як активна учасниця культурного кола: підтримує розмови про творчість і відповідальність, допомагає показати роль однодумців і спільнот у формуванні позиції людини.</p>

  <p><b>Олена Пчілка (Ольга Косач, 1849–1930).</b> Українська письменниця, видавчиня, фольклористка й громадська діячка; одна з провідних діячок українського культурного руху. Мати Лесі Українки, послідовно підтримувала українську мову, освіту і виховання, працювала над розвитком української культури. <br><b>Роль у сюжеті:</b> уособлює роль сім’ї як опори й інституту соціалізації: підкреслює значення виховання, дисципліни, освіти та захисту гідності, впливає на напрям вибору й внутрішню стійкість героїв.</p>

  <p><b>Микола Лисенко (1842–1912).</b> Український композитор, піаніст, диригент, педагог і збирач фольклору; засновник української національної композиторської школи. Працював над музичним оформленням української культури, підтримував розвиток мови й мистецтва. <br><b>Роль у сюжеті:</b> представляє авторитет культурного середовища й приклад самореалізації через працю в мистецтві; підсилює тему спільної справи й того, як культура стає способом підтримки та дії в складних умовах.</p>
