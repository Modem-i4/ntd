<p><b>Мета:</b> Сформувати цілісне уявлення про причини, перебіг і наслідки литовсько-московських війн кінця XV — початку XVI ст.; показати роль Костянтина Острозького в цих подіях і окреслити його історичний образ; розкрити перебіг та значення битви під Оршею (1514 р.); пояснити зміст і вплив московських ідеологічних наративів на політику регіону.</p>

