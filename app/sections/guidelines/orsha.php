<h2>Персонажі:</h2>

<p><b>Гравець, Головний герой, Інтерчасер/ка (в грі: посланець)</b></p>
<p>Коротко про «особу». Ігрова роль учня/користувача, що діє від імені посланця із Вільна.</p>
<p>Роль у сюжеті. Передає накази між підрозділами, збирає відомості у вартового, писаря, кримського союзника та гармашa. Узгоджує сигнали для прихованого мосту, засідки артилерії та вдаваного відступу під Оршею (1514 р.). У вирішальний момент підтверджує готовність підрозділів; після бою передає звіт Костянтину Острозькому.</p>

<p><b>Костянтин Острозький (роки життя: бл. 1460–1530 рр.), військовий і державний діяч Великого князівства Литовського, Руського і Жемайтського із роду Острозьких, князь, великий гетьман литовський, магнат тощо.</b></p>
<p>Коротко про особу. Один із найвизначніших полководців Великого князівства Литовського та руської знаті. Брав участь у війнах ВКЛ із московією наприкінці XV — на початку XVI ст., пережив полон після поразки на Ведроші (1500 р.), повернувся до служби й очолив війська у «великій» кампанії 1512–1522 рр. Переміг під Оршею (1514 р.), за що сучасники називали його «Сципіоном Руським»; опікувався православними святинями, меценатував храмам у Вільні та на Волині, підтримував міста й оборону прикордоння.</p>
<p>Роль у сюжеті. Центральний співрозмовник і стратег: розкриває задум бою (прихований міст, засідка артилерії, вдаваний відступ), збирає від гравця підтвердження готовності підрозділів і підбиває підсумки після битви під Оршею (1514 р.).</p>

<p><b>Писар, очільник польової канцелярії</b></p>
<p>Коротко про «особу». Канцелярський урядовець війська ВКЛ: веде листування, збирає розвіддані, тримає хронологію кампаній, знає розклад сил і маршрутні накази.</p>
<p>Роль у сюжеті. Надає гравцеві стислий таймлайн і карту завдань, передає записки та уточнює порядок дій перед Оршею (1514 р.).</p>

<p><b>Місцевий дрібний князь</b></p>
<p>Коротко про «особу». Узагальнений образ русько-литовського можновладця з прикордонних земель: має власний загін, опікується містами, шляхами постачання та обороною краю; його лояльність важлива для успіху кампаній.</p>
<p>Роль у сюжеті. Обговорює з гравцем підхід загонів, постачання і взаємодію з іншими частинами війська під Оршею (1514 р.).</p>

<p><b>Гармаш (артилерист)</b></p>
<p>Коротко про «особу». Представник нової для Східної Європи військової спеціальності ранньомодерної доби: відповідає за обслуговування та розміщення гармат, боєприпаси, сигнали для залпу та взаємодію з кіннотою й піхотою.</p>
<p>Роль у сюжеті. Роз’яснює гравцеві, як працює засідка артилерії, де стоятимуть знаряддя і коли подавати сигнал, щоби втягнути ворога під вогонь.</p>

<p><b>Кримський татарин</b></p>
<p>Коротко про «особу». Воїн із Кримського ханату, якого після полону під Вишнівцем (1512 р.) прийнято до війська Костянтина Острозького. Уособлює кримських татар союзників ВКЛ (липок). Демонструє милостивість Острозького, який поселив полонених у своєму місті і навіть дозволив звести першу на Волині мечеть.</p>
<p>Роль у сюжеті. Пояснює, чому став союзником Острозького, ділиться розвідданими про рух московитів і узгоджує з гравцем маневри в бою під Оршею (1514 р.).</p>
