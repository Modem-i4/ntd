<p><b>Тема:</b> Українське козацтво у першій чверті XVII ст. Петро Конашевич-Сагайдачний.</p>

<p><b>Мета:</b> Сформувати уявлення про становище та роль козацтва в першій чверті XVII ст.; показати постать і реформи Петра Конашевича-Сагайдачного, стратегію морських і суходільних походів козаків; пояснити причини, перебіг і наслідки Хотинської війни 1620–1621 рр.</p>

<p><b>В результаті проходження гри учні:</b></p>
<ul>
    <li>Пояснюють стан і роль козацтва в першій чверті XVII ст., розуміють правовий статус та описують участь козаків у війнах Речі Посполитої.</li>
    <li>Характеризують Петра Конашевича-Сагайдачного як воєначальника й реформатора та пояснюють його роль у захисті православ’я і міжнародній політиці.</li>
    <li>Описують «добу героїчних походів» козаків: стратегію морських рейдів, ключові акції та оцінюють їхні наслідки.</li>
    <li>Аналізують причини, перебіг і підсумки Хотинської війни 1620–1621 рр. і битви 1621 р.</li>
    <li>Оцінюють політичні результати: злам міфу про непереможність османського війська, зростання престижу Речі Посполитої, посилення легітимації козацтва і вплив на православні інституції.</li>
</ul>

<p><b>Тема сюжету (оповідь):</b> Козацький табір під Хотином, вересень 1621 р.: пошук «слабкого місця» в османській обороні, узгодження нічної вилазки з Яном-Каролем Ходкевичем, бій і підсумкове перемир’я, що зупиняє наступ Османської імперії.</p>

