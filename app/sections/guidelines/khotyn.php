<h2>Персонажі:</h2>

<p><b>Головний герой, Інтерчасер</b></p>
<p>Коротко про «особу». Узагальнений образ посланця-розвідника при козацькому штабі початку XVII ст.; володіє базовими військово-польовими навичками, уміє координувати зв’язок між підрозділами.</p>
<p>Роль у сюжеті. Допомагає з розвідданими, узгоджує нічні та «негодні» вилазки, перевіряє готовність редутів і доповідає Петрові Конашевичу-Сагайдачному та Яну-Каролю Ходкевичу.</p>

<p><b>Петро Конашевич-Сагайдачний (роки життя: бл. 1582–1622 рр.), гетьман Війська Запорозького</b></p>
<p>Коротко про особу. Український (запорозький) полководець і політичний діяч, православний шляхтич. Протягом 1616–1622 рр. чотири рази (з перервами) був обраний гетьманом Війська Запорозького. Реформатор козацького війська: посилив роль піхоти й артилерії. Підтримував братства та відновлення православної ієрархії (1620 р.), очолив похід на москву (1618 р.); поранений під Хотином 1621 р., помер 1622 р.</p>
<p>Роль у сюжеті. Керує козацькими силами, ухвалює рішення щодо табору, редутів і вилазок; координує дії з коронним військом, задає тон обороні.</p>

<p><b>Козак (із самопалом) — узагальнений образ піхотинця</b></p>
<p>Коротко про «особу». Стрілець початку XVII ст.: рушниця/самопал, шабля; вишкіл до бою «за валом» і в малих групах; дисципліна та чергування біля редутів.</p>
<p>Роль у сюжеті. Пояснює порядок служби в таборі, принцип «лави», участь у нічних вилазках, роботу з порохом і боєзапасом.</p>

<p><b>Човняр — судновий козак</b></p>
<p>Коротко про «особу». Учасник морських і річкових рейдів: робота на «чайці», веслування, швартування, висадка десантів, знання берегових укріплень і переправ.</p>
<p>Роль у сюжеті. Дає уявлення про логістику та мобільність козацтва, розповідає про рейдову тактику й переправи, допомагає з нічним маневром біля Дністра.</p>

<p><b>Ян-Кароль Ходкевич (роки життя: 1560–1621 рр.), великий гетьман литовський</b></p>
<p>Коротко про особу. Військовий і політичний діяч Речі Посполитої руського походження, великий гетьман литовський 1605–1621 рр. Один із найвизначніших полководців Речі Посполитої початку XVII ст.; командував коронно-литовським військом у Хотинській війні 1620–1621 рр.; помер у таборі під Хотином у 1621 р.</p>
<p>Роль у сюжеті. Головний партнер козацького командування: узгоджує спільний план оборони, артилерію й взаємодію таборів; ставить завдання Інтерчасеру щодо зв’язку і розвідки.</p>
