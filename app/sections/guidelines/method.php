<h3>Представляємо вам персонажів сценарію!</h3>
<p>Ви можете змінювати їх під потреби вашого сюжету, але вони ж можуть вас надихнути на частинку історії.</p>
<p>Не прив'язуйтеся до статі. Архітектора чи князя може зіграти як хлопець так і дівчина.</p>
<p>Початкова точка сюжету – нас відправили знайти шпигуна. Кінцева точка – шпигуна розсекречено; він признається, що помилявся.</p>