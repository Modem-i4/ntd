        <li><b>Аспект інтеграції:</b> шкільна громада, взаємодія учнів–учителів–батьків–адміністрації, учнівське самоврядування, зв’язок школи й місцевої громади.</li>
        <li><b>Як використати гру:</b> пов’яжіть події/вибори в грі з тим, як у школі ухвалюються рішення: хто має право голосу, як збирати пропозиції, як обґрунтувати рішення і як домовлятися; порівняйте “сильне одноосібне рішення” і “рішення через участь” — що дає кращий результат і чому.</li>
