  <h2 class="text-xl font-bold">Персонажі:</h2>

  <p><b>Головний герой, Інтерчасер/ка.</b> Ігровий образ учня/користувача — мандрівника в часі, який ставить запитання, уточнює деталі й допомагає розібратися в подіях. <br><b>Роль у сюжеті:</b> супроводжує події, спілкується з героями, допомагає ухвалювати рішення та підсумовує, що означають ці кроки для створення організації й спільної дії.</p>

  <p><b>Петро Франко (1890–1941).</b> Український педагог, письменник, науковець-хімік, громадський діяч, один із засновників «Пласту», син Івана Франка. Працював у сфері освіти й молодіжного виховання, був пов’язаний із розвитком скаутського руху в Галичині. Учасник національно-визвольних змагань 1917—1920 рр. <br><b>Роль у сюжеті:</b> пояснює ідею молодіжної організації, підкреслює важливість дисципліни, навичок і служіння; допомагає героям бачити сенс правил і спільних цінностей.</p>

  <p><b>Іван Чмола (1892–1941).</b> Український військовий і громадський діяч. До 1914 року — один з основоположників і співзасновників «Пласту» та стрілецького руху; з 1914 року — старшина Легіону УСС, згодом організатор і полковник Січових Стрільців у Києві; 1919 року — командант куреня, а потім коша Січових стрільців. По війні — гімназійний учитель у Галичині. <br><b>Роль у сюжеті:</b> підсилює “практичний” бік організації: чому важливі вишкіл, порядок і готовність діяти в складних умовах; допомагає команді приймати зібрані рішення.</p>

  <p><b>Олександр Тисовський (1886–1968).</b> Український галицький педагог, основоположник й організатор Пласту, відомий як автор пластового підручника «Життя в Пласті» й один із творців системи виховання через дію. Доктор біологічних наук, дійсний член НТШ, професор Українського Таємного Університету. <br><b>Роль у сюжеті:</b> пояснює, навіщо організації потрібні чіткі принципи й правила, як формується статут і як виховання працює через конкретні справи та відповідальність.</p>

  <p><b>Олена Степанів (1892–1963).</b> Українська громадська діячка й військова, педагогиня, одна з найвідоміших жінок-учасниць українських збройних формацій у час Першої світової війни. Хорунжа Українських Січових Стрільців (УСС). <br><b>Роль у сюжеті:</b> показує приклад лідерства й відповідальності; підкреслює, що громадянська активність і служіння спільноті — це реальні дії, а не лише слова.</p>

  <p><b>Пластун (узагальнений образ учасника «Пласту» початку ХХ ст.).</b> Колективний образ юнака/юначки пластового руху, що дотримується принципів добровільності, самоорганізації, дисципліни, взаємодопомоги та служіння громаді. <br><b>Роль у сюжеті:</b> допомагає показати “звичайного учасника” — як працює команда на практиці, як виконуються домовленості, і що саме робить організацію живою та дієвою.</p>
