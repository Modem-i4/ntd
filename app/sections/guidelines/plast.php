  <p><b>Тема:</b> Дитячі й молодіжні громадські об’єднання</p>

  <p><b>Мета:</b> Сформувати уявлення про те, як працюють громадські організації, які їхні основні принципи та як люди можуть об’єднуватися й діяти разом у громаді.</p>

  <p><b>У результаті проходження гри учні:</b></p>
  <ul class="list-disc pl-6 space-y-1">
    <li>пояснюють, що таке громадська організація (ГО), чим вона відрізняється від політичної партії та комерційних структур, і розуміють, навіщо суспільству потрібні ГО;</li>
    <li>розуміють, для чого потрібен статут та як він допомагає організації у практичному вимірі;</li>
    <li>описують базові принципи роботи неприбуткових організацій і пояснюють, чому вони важливі;</li>
    <li>пояснюють основні етапи створення ГО та розуміють, коли достатньо діяти як гурток, а коли потрібна офіційна реєстрація;</li>
    <li>розрізняють громадянську участь і волонтерство, наводять приклади різних рівнів участі;</li>
    <li>вміють планувати простий соціальний проєкт: від проблеми до результату.</li>
  </ul>

