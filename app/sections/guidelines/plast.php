        <li><b>Як використати гру:</b> порівняйте “потреби” і “бажання” у прикладах з гри; обговоріть, як рішення в грі впливають на добробут людей/громади; зробіть висновок, чому енергоефективність і ощадливість — це не лише “про гроші”, а й про безпеку та стабільність.</li>
