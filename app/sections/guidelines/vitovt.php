<p><b>Тема:</b> Русько-українські землі у складі Великого князівства Литовського протягом останньої чверті XIV – першої половини XV ст. Правління князя Вітовта.</p>

<p><b>Мета:</b> Сформувати уявлення про становище українських земель у складі Великого князівства Литовського протягом останньої чверті XIV – першої половини XV ст., зокрема в період правління князя Вітовта; ознайомити з особливостями політичного устрою ВКЛ, політикою Вітовта та династії Гедиміновичів загалом, відносинами з Польщею та спробами зберегти самостійність держави.</p>

<p><b>В результаті проходження гри учні:</b></p>
<ul>
    <li>Пояснюють місце й статус русько-українських земель у системі ВКЛ та пов’язують це з територіальною динамікою (розширення ВКЛ до Чорного моря), безпекою шляхів і торгівлею.</li>
    <li>Характеризують політику Гедиміновичів та Вітовта зокрема щодо русинів (руське право, віра, співіснування конфесій, місцеві звичаї) і оцінюють роль місцевих еліт і міських громад у функціонуванні держави.</li>
    <li>Аналізують династичну тяглість Гедиміновичів і пояснюють рамки союзу з Польщею (Кревська та Городельська унії) як основу балансу між автономією ВКЛ і союзницькими зобов’язаннями.</li>
    <li>Пояснюють дипломатично-військовий контекст (зокрема Грюнвальд 1410 р.) та роблять висновок, як перемоги й союзи впливали на політичну вагу Литви та статус руських земель.</li>
    <li>Інтерпретують Луцький з’їзд 1429 р. як аспект легітимності влади та боротьби за самостійність ВКЛ, оцінюючи можливі наслідки різних рішень для регіону.</li>
</ul>

